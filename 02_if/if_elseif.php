<?php
// نمره‌ی دانش‌آموز (این مقدار را تغییر بدهید و خروجی را ببینید)
$nomre = 22;

// بررسی محدوده نمره
if ($nomre < 0) {
    echo "❌ خطا: نمره نمی‌تواند منفی باشد.";
}
elseif ($nomre < 10) {
    echo "📕 وضعیت: مردود";
}
elseif ($nomre < 18) {
    echo "📘 وضعیت: قبول";
}
elseif ($nomre <= 20) {
    echo "📗 وضعیت: ممتاز";
}
else { // یعنی $nomre > 20
    echo "❌ خطا: نمره نمی‌تواند بیشتر از 20 باشد.";
}
